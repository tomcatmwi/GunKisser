$(document).ready(function() {
  
//  --------------------------------------------------------------------------------------------------
//  close button click event
//  --------------------------------------------------------------------------------------------------

  $('#btnClose').click(function(e) {
    e.preventDefault();
    location.href = api_url+'/weapons_info/'+$('#id').val();
  });

//  --------------------------------------------------------------------------------------------------
//  print button click event
//  --------------------------------------------------------------------------------------------------

  $('#btnPrint').click(function(e) {
    e.preventDefault();
    window.print();
  });


});

